package main

import (
	"fmt"
)

func main() {
	// A few of the common operators
	// equivalence
	a := 77 == 42
	// less than
	b := 77 < 42
	//greater than
	c := 77 > 42
	// lesser than or equal to
	d := 77 <= 42
	// greater than or equal to
	e := 77 >= 42
	// not equal to
	f := 77 != 42
	
	fmt.Println(a)
	fmt.Println(b)
	fmt.Println(c)
	fmt.Println(d)
	fmt.Println(e)
	fmt.Println(f)
}